export function translate(language: "es" | "en", key: string, defaultValue = key) {
  const keys = key.split(".");
  let value: any = translations[language];
  
  for (const k of keys) {
    if (value?.[k]) {
      value = value[k];
    } else {
      return defaultValue;
    }
  }
  
  return typeof value === "string" ? value : defaultValue;
}
